function maxNumber(num1,num2){
    if (num1>num2) {
        return num1
    }else {
        return num2
    }
}
const max = maxNumber(15,49);
const max2 = maxNumber(45,90);
const ultimatemax = maxNumber(max,max2);
console.log(ultimatemax);
console.log(max);

